package it.studio_java8.interfaccia_funzionale.prove;

public class Mela {
    private Integer peso;
    private String colore;

    public Mela(Integer peso, String colore) {
        super();
        this.peso = peso;
        this.colore = colore;
    }

    public Integer getPeso() {
        return peso;
    }

    public String getColore() {
        return colore;
    }
}
